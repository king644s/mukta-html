                <!-- Introduction -->
                <section class="blog-section" data-fade>
                  <p class="blog-intro">
                    The current escalation in the Middle East (March 2026) has moved beyond regional skirmishes into a full-scale disruption of some of the world’s most critical trade veins.
                    For Indian exporters, the “West Asia problem” is no longer a distant geopolitical risk — it’s a daily line item on their balance sheets.
                  </p>
                  <p>
                    With the Strait of Hormuz on edge and Red Sea routes reverting to “emergency mode”, Indian supply chains are being forced into expensive detours, tougher contract negotiations, and continuous risk re-pricing.
                  </p>
                </section>

                <!-- Image Figure -->
                <figure class="blog-figure" data-fade>
